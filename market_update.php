                <h2>Double-digit increase in sales</h2>

                <h3>Still some pent-up demand</h3>

                <p>The frigid weather earlier this year didn’t slow down the real estate market.</p>

                <p>Anyone who decided to wait for the spring weather missed out on a lot of homes for sale. This year’s market started strong and then got another boost when the Bank of Canada lowered its overnight interest rate in January. People who thought the market had no pent-up demand were proven wrong as sales took off again.</p>

                <p>The trend continued in March, as <strong>sales jumped by 11 per cent in the GTA compared to last year</strong>. New listings were up too but by only 5.5 per cent. Toronto Real Estate Board (TREB) president Paul Etherington believes there would have been even more sales if it weren’t for a lack of good listings.</p>

                <p><i>“The fact that sales growth has continued to outpace listings growth suggests that there is pent-up demand in the marketplace, especially for singles, semis and townhouses,”</i> says Jason Mercer, TREB’s director of market analysis. “This is why we have experienced a sustained period of strong price growth for low-rise types.”</p>

                <p>TREB data shows that a greater share of high-end detached homes sold in March than sold at the same time last year, resulting in a bump in average prices. In the City of Toronto, the average detached home now sells for more than $1 million. In the 905 regions, detached houses sell for an average price of just over $700,000. For all housing types, the average selling price for the month of March was $613,933, an increase of 10 per cent compared to a year ago.</p>

                <p>While low mortgage rates continue to drive the market, other factors keep Toronto’s real estate market hopping. Immigration to the city is steady, including people returning from out west after the drop in oil prices eliminated job prospects there.</p>

                <p>Manufacturing and exporting industries are benefiting from lower energy costs and the depreciated Canadian dollar which is already leading to more job creation. The Ford plant in Oakville, for example, has added 1,400 positions in the last six months.</p>

                <p>Naysayers suggest that high household debt is a major concern in this country, which could lead to problems in the housing market. Recently Sal Guatieri, senior economist for BMO Capital Markets, said that while there are reasons for concern “the debt isn’t as onerous as some believe.”</p>

                <p>Guatieri continues, <i>“Tougher mortgage rules, combined with warnings from the Bank of Canada, Ottawa and the IMF, have kept borrowing on a tighter leash. Barring a further decline in interest rates, credit is unlikely to accelerate…”</i> Guatieri believes most Canadians don’t have a debt problem <i>“and will have little trouble managing their finances when interest rates finally normalize.”</i></p>

