            <h2>Milton</h2>
            <p>The town of Milton is part of the Halton Region in the GTA and is one of the fastest growing communities in Ontario. Milton strikes the right balance of urban and rural living, with a variety of amenities and new developments each year that are driving population growth. Situated on the edge of the Niagara Escarpment, citizens can enjoy the scenic views and incredible waterfalls of the Bruce Trails. The ever growing town has become a popular place for growing families due to it’s affordability and increasing land value.</p>

