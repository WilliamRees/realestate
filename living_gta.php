            <p>Toronto is Canada’s largest city per capita in Ontario and is home to 2.8 million residents. A multicultural city, Toronto is the prime example of Canada’s melting pot population welcoming residents from all over world. Toronto’s extensive public transit system, efficient waste management that is constantly rising in green standards and some of the best schools and hospitals in Canada makes this city Ontario’s Crown Jewel. The city is vibrant with lots of big ticket events, festivals and home to some of Canada’s legendary sports teams.</p>

            <h2>Mississauga</h2>
            <p>Mississauga is Canada’s 6th largest city and is fastest growing city. Recognized as the safest city in Ontario, Mississauga brings peace of mind and security to its residents. A predominantly suburban city the downtown core is home to a lot of fine dining restaurants, shopping centre, parks, entertainment, arts centre, college campus, and city hall where several events are held year round. Mississauga is home to close to 55,000 registered businesses and a thriving place for economical opportunities.</p>

            <h2>Brampton</h2>
            <p>Brampton is one of Canada’s largest and fastest growing cities, with a current population of over 600,000 people. This lively and diverse city has over 209 distinct ethnic backgrounds which are highlighted through an extensive mix of restaurants, shops, festivals and sport opportunities. Brampton is known as Canada’s Flower City due to the acres of land that are devoted to parks, nurseries, and glass greenhouses. These features simply add to the beauty of this city and enhance the outdoor atmosphere for its citizens.</p>


            <h2>Oakville</h2>
            <p>Oakville is an exciting town located in the Halton Region of the GTA that is constantly growing every year. With many new expansions, Oakville offers a wonderful experience and something for everyone to enjoy. With over 1,420 hectares of parkland, this invigorating town provides a number of trails, dog parks, playgrounds, sports field, splash pads and waterfront parks. Beautifully well kept all year round, Oakville is home to three distinct business areas: Downtown Oakville, Bronte Village and Kerr Village. All located on the shores of Lake Ontario, providing character, vibrant shopping and a number of dining options.</p>

